//其中问题，及解答
//关于 fopen("c:\\num.txt", "r")
//反斜杠的使用: 由于 \ 在字符串中是一个转义字符，如果你想在字符串中表示一个实际的反斜杠，
//需要使用两个反斜杠 \\。这意味着"c:\num.txt"将被解析为 c : 后面接一个转义字符，后面的 n 会被看作换行符，而不是字母 n。
//
//所以 : 使用 fopen("c:\\num.txt", "r") 这种方式，实际上是在字符串中确保 \ 被正确地识别并最终表示为文件路径中的一个普通反斜杠。
//进一步说明
//操作系统的文件路径分隔符 : 在许多操作系统（如Windows）中，文件路径使用反斜杠 \ 作为分隔符，而在Unix / Linux系统中使用正斜杠 / 。在C语言中，为了确保代码的可移植性，尽量避免使用硬编码的反斜杠。
//
//建议的使用方式 : 如果你希望代码在跨平台上更具可移植性，可以考虑使用正斜杠 / ，例如：
//
//fopen("c:/num.txt", "r");
//在大多数情况下，这种方式可以在Windows及其他操作系统中正常工作。
//
//总结
//双反斜杠 \\ 是为了确保反斜杠在字符串中的正确表示。这样，文件路径能够被正确识别和使用。
//补充：
// 读取缓冲区
//C标准库中的文件指针（FILE *）会包含一个缓冲区，用于高效地读取文件。第一次读取时，C库可能会从磁盘读取一小块数据到内存中（即缓冲区），
// 然后后续的读取操作会直接从这个缓冲区中获取数据，而不是每次都去磁盘读取，从而提高效率。
//指针状态
//一旦文件成功打开，C语言库会将内部指针（文件位置指针）设置为文件的开头。这意味着在第一次读取操作时，数据会从文件的第一字符开始读取。
//文件位置指针又叫做隐形的指针
//区分：
//文件指针fp指代整个文件，始终指向文件头
//文件位置指针的作用是标记位置，暂时指向文件头
//用一次getc文件位置指针会向后移动一位
