//文件流是C语言中处理文件输入输出（I / O）的一种抽象概念。它提供了一种将文件当作数据流来读写的方式，使程序能够灵活地处理不同类型的文件（文本文件或二进制文件），并
// 简化了I / O操作。以下是文件流的关键概念：
//
//1. 文件流的定义
//文件流可以被看作是一种数据结构，它表示一个打开的文件。每个文件流都有一个唯一的 FILE 类型的指针，称为 FILE 对象。
//
//2. 文件流的类型
//主要有两种类型的文件流：
//
//文本流（Text Stream） : 以文本形式读写数据。这种流处理的是字符，通常用于处理常规的文本文件。文本流会自动进行字符编码转换，比如将换行符转换为平台特有的表示（如在Windows上将\n转换为\r\n）。
//
//二进制流（Binary Stream） : 以二进制形式读写数据，直接处理字节，不做任何编码转换。适用于图像、音频、视频和任意格式的数据文件。
//
//3. 打开和关闭文件流
//使用 fopen 函数打开文件流，创建一个 FILE 对象。例如：
//
//c
//FILE * file = fopen("example.txt", "r"); // 打开文件用于读取  
//文件流在使用完成后需要通过 fclose 函数关闭，以释放资源：
//
//c
//fclose(file);
//4. 文件流的操作
//文件流使用一系列标准库函数进行操作，包括：
//
//读取 : 如 fread、fgets、fscanf 等。
//写入 : 如 fwrite、fputs、fprintf 等。
//5. 缓冲
//C语言中的文件流通常是缓冲的，这意味着输入和输出操作不是立即进行的，而是先将数据存储在内存中的缓冲区中，以提高效率。当缓冲区满或调用 fclose 时，数据才会被实际写入文件。
//
//6. 文件流状态
//每个文件流都有一个与之关联的状态信息，用于指示流的状态（如是否出错、是否到达文件末尾等）。可以使用 feof 和 ferror 函数检查流状态。
//
//总结
//文件流在C语言编程中扮演着重要角色，它为处理文件提供了一套强大的接口，使得文件的读写操作变得直观和高效。通过理解文件流的概念，可以更好地进行文件管理和数据处理。
