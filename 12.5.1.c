//图的定义：
//图G由顶点集V和边集E组成
//用/V/表示图G中的顶点个数，也称为图G的阶，用/E/来表示图G的边数。
//每一条边都会有顶点链接
//注意：线性表可以是空表，树可以是空树，但图不可以是空，即V一定是非空集，但是E可以是空集
//1.无向图、有向图（一个是有方向的有箭头的，一个是无方向无箭头的）
//2.简单图和多重图
//简单图--1.不存在重复边//2.不存在顶点到自身的边
//*******************
//顶点的度：
//对于无向图：顶点v的度是指依附于该顶点的边的条数，记为TD(v)
//对于有向图来说：
//入度：是以顶点v为终点的有向边的数目，记为ID（V）
//出度：是以顶点v为起点的有向边的数目，记为OD（V）
//顶点v的度就是这两者之和
//********************
//路径--是指某个顶点到某个顶点的之间的顶点序列
//回路--第一个顶点和最后一个顶点相同的路径称为回路或环
//简单路径：顶点不出现重复
//简单回路---除第一个顶点和最后一个顶点外，其余顶点不重复出现回路称为简单回路。
//路径长度：路径上边的数目
//点到点的距离--从顶点n出发到顶点v的最短路径若存在，则此路径的长度称为从u到v的距离。若不存在路径则记该距离为无穷
//联通：两个顶点之间有路径存在（无向图中）
//强连通：两点都有路径两向联通（有向图）
//*************************
//任意两个顶点都是联通的，则该图称为连通图（无相）//考点：n个顶点则至少有n-1条边，如果是非连通图最多有C2(n-1)条边
//同理在有向图中，称为强连通图//考点：对于有n个顶点的有向图，若是强连通图，则最少有n条边。
//子图：
//生成子图：包含了所有顶（不包括所有的边）
// //****************//重点
//联通分量----------无向图的极大联通子图（包含尽可能多的顶点和边）
//强联通分量----------有向图的极大联通子图（包含尽可能多的顶点和边）
//*************************
//连通图的生成树是包含图中全部顶点的一个极小连通子图（便要尽可能地少但要保持联通
//生成森林--在非连通图中，连通分量的生成树构成了非连通图的生成森林
//**************
//边的权---带有权值
//带权网/网
//带权路径长度--路径上的边所有权值之和
//****************
//无向完全图---无向图中任意俩个顶点之间都存在边
//有向完全图---有向图中任意俩个顶点之间都存在方向相反的两条弧
//稀疏图--边少
//稠密图--反之
//树--不存在回路，且联通的无向图
//考点：n个顶点的图，若E>n-1.则一定有回路
//有向树---一个顶点的入度为0，其余顶点的入度均为1的有向图